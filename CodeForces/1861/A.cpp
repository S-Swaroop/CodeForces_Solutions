void solve() {
    string s;
    cin >> s;
    int index_of_one = s.find('1');
    int index_of_three = s.find('3');
    if (index_of_one > index_of_three) {
        cout << 31 << endl;
    } else {
        cout << 13 << endl;
    }
}

